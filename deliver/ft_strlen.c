#include "libft.h"

int	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (*s)
	{
		i++;
		s++;
	}
	return (i);
}

/*
The strlen() function calculates the length of the string pointed to by
       s, excluding the terminating null byte ('\0')
*/